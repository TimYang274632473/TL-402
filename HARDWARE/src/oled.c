/* LCD开显示		*/
void OLED_On(char Charging)
{
	cnt_1s.LCD_TurnOff_Cnt=0;
	
	if(LCD_F.b.Power_OFF_Flag) return;
	
	if(LCD_F.b.On_Off_Flag == false)
	{
		OLED_CS_low();
		Write_Command(0xAF);     														// Set Display On 
		OLED_CS_high();
		LCD_F.b.On_Off_Flag = true;
	}
		
	if(MCU_F.b.GSM_Init_Flag)		return;
		
	if(LCD_F.b.KeepOn_Cnt_Flag)															// LED常亮
	{
		LCD_F.b.Turnoff_Cnt_Flag = false;
		LCD_F.b.Charging_Cnt_Flag	= false;	
	}
	else
	{		
		if(Charging)																	// 需要清除充电介面
		{
			LCD_F.b.Turnoff_Cnt_Flag = true;
			LCD_F.b.Charging_Cnt_Flag = false;
		}
		else
		{		
			LCD_F.b.Turnoff_Cnt_Flag = true;
		}
	}
}

/* 不在常亮和充电条件下熄屏			*/
void OLED_Off(unsigned char ForceOff)																		
{	
	if((ForceOff == 0) && LCD_F.b.Charging_Flag)										// 充电状态	
	{
		cnt_1s.LCD_Charging_Cnt = 0;
		LCD_F.b.Charging_Cnt_Flag = true;												// 启动显示充电介面
	}
	else
	{
		if(LCD_F.b.On_Off_Flag)															// 之前是亮屏
		{
			OLED_CS_low();
			Write_Command(0xAE);     													// Set Display Off
			OLED_CS_high();	
			LCD_F.b.On_Off_Flag = false;
		}
	}
}

// 更新显存到LCD	
// GSM_P.Oled_Set_Direction 0:按正向显示，1: 按反向显示, 2:按BMA方向显示
void OLED_Refresh_Gram(unsigned char Dir)
{
	u8 i,n;

	OLED_CS_low();
	
	if(Dir == 2)										// 按BMA方向自动旋转LED显示角度
	{		
		i = BM250_GET_Slop();							// 获取BMA250角度
		
		if(LCD_F.b.Angle_Now_Flag)						// 如果之前为正向显示
		{
			if(i > 100)	
			{
				LCD_F.b.Angle_Now_Flag = 0;
			}																
		}
		else											// 如果之前为反向显示
		{		
			if(i < 80)						
			{				
				LCD_F.b.Angle_Now_Flag = 1;	
			}
		}
		
		i = LCD_F.b.Angle_Now_Flag;
	}
	else												// 按自定义方向显示角度
	{
		i = Dir & 0x01;
	}
		
	if(i != LCD_F.b.Angle_Buf_Flag)						// 如果此次与上次显示方向不同,则重设指令,否则不处理,以省电
	{	
		LCD_F.b.Angle_Buf_Flag = i;
		
		if(LCD_F.b.Angle_Buf_Flag) 						// 反向显示
		{
			Write_Command(0xA1);     					//Segment Remap	
			Write_Command(0xC0);     					//Sst COM Output Scan Direction	
			Write_Command(0xDA);     					//common pads hardware: alternative	
		}
		else											// 正向显示
		{
			Write_Command(0xA0);     					//Segment Remap	 
			Write_Command(0xC0);     					//Sst COM Output Scan Direction	
			Write_Command(0xCA);     					//common pads hardware: alternative
		}

		Write_Command(0x12);
	}
	
	for(i=0;i<8;i++)  
	{  
		Write_Command (0xb0+i);    						// 设置页地址（0~7）
		Write_Command (0x02);      						// 设置显示位置―列低地址
		Write_Command (0x10);      						// 设置显示位置―列高地址   
		for(n=0;n<128;n++) Write_Data(OLED_GRAM[n][i]); 
	} 

	OLED_CS_high();		
}



//画点 
//x:0~127
//y:0~63
//t:1 填充 0,清空				   
void OLED_DrawPoint(u8 x,u8 y,u8 t)
{
	u8 pos,bx,temp=0;
	if(x>127||y>63)return;//超出范围了.
	pos=7-y/8;
	bx=y%8;
	temp=1<<(7-bx);
	if(t)OLED_GRAM[x][pos]|=temp;
	else OLED_GRAM[x][pos]&=~temp;	    
}
//x1,y1,x2,y2 填充区域的对角坐标
//确保x1<=x2;y1<=y2 0<=x1<=127 0<=y1<=63	 	 
//dot:0,清空;1,填充	  
void OLED_Fill(u8 x1,u8 y1,u8 x2,u8 y2,u8 dot)  
{  
	u8 x,y;  
	for(x=x1;x<=x2;x++)
	{
		for(y=y1;y<=y2;y++)OLED_DrawPoint(x,y,dot);
	}													    
}
//在指定位置显示一个字符,包括部分字符
//x:0~127
//y:0~63
//mode:0,反白显示;1,正常显示				 
//size:选择字体 12/16/24
void OLED_ShowChar(u8 x,u8 y,u8 chr,u8 size,u8 mode)
{      			    
	u8 temp,t,t1;
	u8 y0=y;
	u8 csize=(size/8+((size%8)?1:0))*(size/2);		//得到字体一个字符对应点阵集所占的字节数
	chr=chr-' ';//得到偏移后的值		 
    for(t=0;t<csize;t++)
    {   
		if(size==12)temp=asc2_1206[chr][t]; 	 	//调用1206字体
		else if(size==16)temp=asc2_1608[chr][t];	//调用1608字体
//		else if(size==24)temp=asc2_2412[chr][t];	//调用2412字体
		else return;								//没有的字库
        for(t1=0;t1<8;t1++)
		{
			if(temp&0x80)OLED_DrawPoint(x,y,mode);
			else OLED_DrawPoint(x,y,!mode);
			temp<<=1;
			y++;
			if((y-y0)==size)
			{
				y=y0;
				x++;
				break;
			}
		}  	 
    }          
}


//m^n函数
u32 mypow(u8 m,u8 n)
{
	u32 result=1;	 
	while(n--)result*=m;    
	return result;
}				  
 
