/* 显示电量		*/
void OLED_Show_Battery(u8 soc)
{
	u8 level,n;	
		
	draw_level_line(0,17,3,1);							
	draw_level_line(0,17,11,1);
	draw_vertical_line(3,11,0,1);
	draw_vertical_line(3,11,17,1);
	draw_vertical_line(5,9,18,1);
	
	level=1+soc/20;
	if(level>5) level=5;
	
	for(n=0;n<level;n++)												// 显示五格电量
	{
		OLED_Fill(2+n*3,5,3+n*3,9,1);
	}
	
	if(LCD_F.b.Charging_Flag)											// 如果正在充电
	{
		draw_line(7,6,9,4,1);
		draw_line(8,6,10,4,1);
		draw_line(6,7,11,7,1);
		draw_line(7,10,9,8,1);
		draw_line(8,10,10,8,1);
	}
	
	if(soc==100)
	{
		OLED_ShowNum(22,2,soc,3,12);
		OLED_ShowChar(40,2,'%',12,1);
	}
	else if(soc<10)
	{
		OLED_ShowNum(22,2,soc,1,12);
		OLED_ShowChar(28,2,'%',12,1);	
	}		
	else
	{
		OLED_ShowNum(22,2,soc,2,12);
		OLED_ShowChar(33,2,'%',12,1);
	}
}

